# Functions
def salas_function():
  print("Joe Salas")
  print("Jackie Salas")
  print("Julian Salas")
  print("Jasper Salas")

salas_function()

def salas_function_arg(arg1, arg2, arg3, arg4):
  print(f"{arg1}, {arg2}, {arg3}, {arg4}")

salas_function_arg("One","Two","Three","Four")
salas_function_arg("1","2","3","4")
salas_function_arg("Uno","Dos","Tres","Cuatro")

def salas_function_arg_desc(arg1, arg2, arg3, arg4):
  print(f"arg1: {arg1}, arg2: {arg2}, arg3: {arg3}, arg4: {arg4}")

salas_function_arg_desc("One","Two","Three","Four")
