# two arguments in this function
def print_two(arg1, arg2):
  print(f"arg1: {arg1}, arg2: {arg2}")

# this just takes on argument
def print_one(arg1):
  print(f"arg1: {arg1}")

# this one takes no arguments
def print_none():
  print("I got nothin'.")

print_two("Zed", "Shaw")
print_one("First!")
print_none()