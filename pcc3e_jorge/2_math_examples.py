# Math Examples

# INTEGERS
print(2 + 3)
print(3 - 2)
print(2 * 3)
print(3 / 2)
print(3 ** 2)
print(3 ** 3)
print(10 ** 6)

print(2 + 3*4)
print((2 + 3) * 4)

# FLOATS
print(0.1 + 0.1)
print(0.2 + 0.2)
print(2 * 0.1)
print(2 * 0.2)

print(0.2 + 0.1)
print(3 * 0.1)

# INTEGERS AND FLOATS
# anytime you divide, your result will be a float
# anytime you use a float, your result will be a float
print(4 / 2)
print(1 + 2.0)
print(2 * 3.0)
print(3.0 * 2)

# UNDERSCORES
# underscores get ignored in numbers. it only helps for us to read the number
universe_age = 14_000_000_000
print(universe_age)

# MULTIPLE ASSIGNMENT
# assign values to multiple variables at once
x, y, z = 0, 1, 2
print(f"{x} - {y} - {z}")

